    <h1 class="text-uppercase text-center">illuminator</h1>
    <p>Illuminator badge is given to agents stats of total MU that they captured while creating fields. It’s retroactive so it will count all your previous MU captures that you had.</p>
    <p>Illuminator badge has 5 tiers and each tier has a required number of MU to achieve it.</p>
    <ul>
        <li>Bronze – 5 000 MU</li>
        <li>Silver – 50 000 MU</li>
        <li>Gold – 250 000 MU</li>
        <li>Platinum – 1 000 000 MU</li>
        <li>Onyx – 4 000 000 MU</li>
    </ul>






