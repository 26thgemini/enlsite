    <h1 class="text-uppercase text-center">sojourner</h1>
    <p>
        The Sojourner medal is awarded for performing at least one hack every day for a certain number of days.
        The number of days is computed from the moment of your first hack while the day counter is at 0. Starting from that moment, you need to ensure that every hack is no more than 24 hours after the previous one; otherwise, the counter will reset.
    </p>
    <ul>
        <li>Bronze – 15 days</li>
        <li>Silver – 30 days</li>
        <li>Gold – 60 days</li>
        <li>Platinum – 180 days</li>
        <li>Onyx – 360 days</li>
    </ul>
