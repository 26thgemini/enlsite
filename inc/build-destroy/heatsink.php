    <h3>Heatsink</h3>
    <div class="thumbnail">
        <img src="img/build-destroy/heatsink.png" alt="heatsink" class="img-thumbnail img-responsive"/>
        <div class="caption text-justify">
            <p>
                A Heat Sink can be used to decrease the duration between Portal hacks. It’s used to cooldown a portal so
                it can be hacked again sooner. The Heat sink is one of the 5 new Ingress Portal Mods from the Ingress
                v1.28.1. The Heat sink and  Multi-hack will create some great portal farming areas.
            </p>
            <p>
                As of March 20, 2014, a new ability was added to the Heat Sink, thanks to IQTech Research.  An article
                was written to break down the changes into greater detail, Next Generation of Heat Sinks.  In short,
                addition of a heat sink to a portal will reset both the cooldown timer, as well as reset the burnout
                counter.  Both of these resets only apply to the agent deploying the heat sink.
            </p>
            <p>
                Depending on its type, each deployed Heat Sink is decreasing the time interval between 2 hacks. Here is
                the exact property on each type:
            </p>
            <ul>
                <li>Common = +20% Portal Cooldown Decrease</li>
                <li>Rare = +50% Portal Cooldown Decrease</li>
                <li>Very rare = +70% Portal Cooldown Decrease</li>
            </ul>
            <p>
                With every deployed Heat Sink you get less and less benefit from them. This is a way to discourage
                deployment of same mods on portal. We figured out the Niantic math again, here is how it goes.
                The cooldown wait period with no mods is 300 seconds. You can’t decrease the cooldown time to the negative,
                you would destroy the whole universe! Here is how you calculate the decrease:
            </p>
            <ul>
                <li>Common Heat Sink = 20% decrease or 0.2</li>
                <li>Rare Heat Sink = 50% decrease or 0.5</li>
                <li>Very Rare Heat Sink = 70% decrease or 0.7</li>
            </ul>
            <p>
                The diminished returns values are as follows:
            </p>
            <ul>
                <li>First = x1 speed</li>
                <li>Second = x0.5 speed</li>
                <li>Third = x0.5 speed</li>
                <li>Forth = x0.5 speed</li>
            </ul>
            <p>
                So maximum cooldown decrease is 91.76% and that is 24.72s seconds between 2 hacks!
                ith these new numbers and confirmed research, there is no difference in the order of deployment,
                so the stats will be the same if you place a Common HS and then a Very Rare HS or a Very Rare HS and then a Common HS.
            </p>
