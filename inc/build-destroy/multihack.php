    <h3>Multi-Hack</h3>
    <div class="thumbnail">
        <img src="img/build-destroy/multihack.png" alt="multihack" class="img-thumbnail img-responsive"/>
        <div class="caption text-justify">
            <p>
                The Multi-hack is used to increase the number of times a Player can Hack a Portal before it goes to the
                Burnout state.  The Multi-hack is one of the 5 new Portal Mods from Ingress v1.28.1. In combination with
                Heat Sinks its a killer mod for big Portal farming!
            </p>
            <p>
                Each deployed Multi-hack will increase the number of times you can hack a Portal depending on its type. Here are the types:
            </p>
            <ul>
                <li>Common = +4 times</li>
                <li>Rare = +8 times</li>
                <li>Very Rare = +12 times</li>
            </ul>
            <p>
                But only one of the rarest MH will add full number of hacks, all others will give 50% of their stats.
                For example 1 R MH and 2 VR MH gives 8/2+12+12/2 = 22 more hacks.
            </p>
            <p>
                So with the initial 4 available hacks, you can get up to 4+12+(12/2)*3 = 34 hacks before sending a  portal
                to the burnout state, and to hack all of them you will need 170 minutes, or almost 3 hours.
            </p>
