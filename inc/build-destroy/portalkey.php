    <h3>Portal Key</h3>
    <div class="thumbnail">
        <img src="img/build-destroy/portalkey.png" alt="portalkey" class="img-thumbnail img-responsive"/>
        <div class="caption text-justify">
            <p>
                The Portal Key is the most important item in Ingress if you want to create links and control fields.
                A Portal key can be acquired in a couple of ways: hacking portals or destroying links and then picking
                it up (Portal keys drop some of the time when you destroy links and they drop at the originating portal
                for that link, not the terminating/destination portal).
            </p>
            <p>
                Each Key is specific to one portal, but you can have multiple keys of the same portal. When hacking a
                portal in hopes of collecting a portal key, your odds of acquiring one through a hack are far  greater
                when you do not currently have that key in your inventory.  If you already have a key to a portal and
                you are needing to farm multiple for an operation, your best bet is to drop the key on the ground, or
                to place the key you are farming into a capsule.
            </p>
            <p>
                In your Items tab in the OPS menu, you can filter your items to show only Portal Keys. They are sorted by
                two different methods.  Either distance from your current position (closest ones are first in the list),
                Or sorting by name. On the portal key you can see its level, faction, current percentage of XM, Xm mods being
                used on the portal, a photo of the portal, portal name and address, and the distance of the portal from your
                current location, as well as the Nav button. The Nav button will direct you on the distance you are from the
                portal you are currently set to navigate to.  Once selected you can go into a portal to see it’s mod’s
                resonator energy levels, and if it’s owned by your faction and you have a key or multiple keys  to it or
                to drop keys.  From this view you  also have the ability  to remote recharge the portal (only if you have
                a key to the portal).
            </p>
            <p>
                With new updates, when viewing keys through the inventory menu. you can now easily see how many keys you have
                for each portal, this can also be seen if you click on a portal and have multiple keys
            </p>
        </div>
