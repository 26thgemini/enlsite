    <h3>Resonator</h3>
    <div class="thumbnail">
        <img src="img/build-destroy/resonator.png" alt="resonator" class="img-thumbnail img-responsive"/>
        <div class="caption text-justify">

            <p>Resonators are key Ingress items for building Portals. They are used to gain control of and level up Portals
                for your Faction. Every resonator contains XM energy, and with every level it contains more XM. Each Portal
                has 8 slots for resonators and you can deploy or upgrade them. They are the only items that decay (lose XM)
                over time.  Like every item it can be dropped, picked up or recycled. They are obtained by Hacking Portals or
                using Passcodes.</p>

            <p>Resonators can range from level 1 to level 8. Each resonator level is visually different and they all have different
                amount of energy that they can hold. The Portal level depends on deployed resonators, they also affect how far you can link.</p>

            <p>
                All Resonators are classified as very common type, meaning your odds of hacking them are very high. Maybe in the future 
                we will see some other types of resonators, but as of now it seems very unlikely.
            </p>
