    <h3>Shields</h3>
    <div class="thumbnail">
        <img src="img/build-destroy/shield.png" alt="shield" class="img-thumbnail img-responsive"/>
        <div class="caption text-justify">
            <p>
                Portal shields are portal mods used to help defend portals from XMP attacks. They enhance the mitigation
                stats of a portal where they are deployed. You can deploy up to 4 shields on one portal. When deployed
                you will see them as an energy dome around the portal. Portal shields are very effective and useful for
                defending important portals.
            </p>

            <p>
                On 3/6/2014, Shields are dropped with new stats:
                <br/>
                <ul>
                    <li>Common: +30 mitigation</li>
                    <li>Rare: +40 mitigation</li>
                    <li>Very rare: +60 mitigation</li>
                </ul>
            </p>
            <p>
                To deploy a portal shield, you need to be at action distance from the portal. That portal will also need
                to be in possession of your faction and have a free mod slot.  As a single agent can only place a max of
                two mods on a portal, care should be taken in which mods are deployed.
            </p>
            <p>
                A portalâ€™s mitigation is a defensive number associated with how much damage is mitigated to the resonators.
                The higher this number, the better chances of your portal surviving until you are able to defend.
                The mitigation caps out on a portal at 95% however, so at least some damage will get through, regardless
                of how shielded a portal may be.  Mitigation may also be enhanced through linking, as each link adds to
                the inherent mitigation numbers for a portal.
            </p>
            <p>
                With these new numbers, a single shield is better able to provide mitigation, thus enabling you to install
                other mods and still have a high mitigation value on a portal.  A portal with 4 links provides 43 mitigation,
                where as a portal with 5 links provides 48 mitigation.  Combine that with a single Very Rare Shield, and
                you get 93 and 98 mitigation respectively.  93 mitigation, being just below the 95 mark, will remain,
                where as 98 mitigation will only provide 95% mitigation to the portal.  Additional shields provide assistance
                in making sure the mitigation stands should a shield fall, but will not raise the mitigation levels any higher.
                This should also be taken into account when deciding how best to protect and use your portals
            </p>
